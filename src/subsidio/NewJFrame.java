    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int estratos;
        
        String nombre;
        nombre = JOptionPane.showInputDialog("Introduce un nombre:");
        
        estratos = Integer.parseInt(JOptionPane.showInputDialog("Cual es su estrato"));
         if (estratos <= 3)
         {
             JOptionPane.showMessageDialog(null,nombre +" usted es un posble beneficiaro del subsido de Vivienda");
      
         }
         else if (estratos <= 5)
         {
             JOptionPane.showMessageDialog(null,nombre +" usted es un posble beneficiaro del subsido alimentario");
         }
         else
         {
             JOptionPane.showMessageDialog(null,nombre +" los subsidios solo estan disponibles hasta estrato 5");
         }   
         System.exit(0); 
    }//GEN-LAST:event_jButton1ActionPerformed

